def separator_like_title(title: str) -> bool:
    return bool(re.fullmatch(r"[═=─—_\-\s]{6,}", clean_text(title)))


def noise_reason_for_entry(entry: BookmarkEntry) -> Optional[str]:
    url = normalize_url(entry.url)
    parsed = urlparse(url)
    host = (parsed.hostname or "").lower()
    if parsed.scheme in {"chrome-extension", "javascript", "data", "about"}:
        return f"scheme:{parsed.scheme}"
    if host in {"separator.site", "separator.mayastudios.com"}:
        return f"host:{host}"
    if separator_like_title(entry.title):
        return "separator-title"
    return None


def noise_reason_for_record(url: str, title: str) -> Optional[str]:
    parsed = urlparse(normalize_url(url))
    host = (parsed.hostname or "").lower()
    if parsed.scheme in {"chrome-extension", "javascript", "data", "about"}:
        return f"scheme:{parsed.scheme}"
    if host in {"separator.site", "separator.mayastudios.com"}:
        return f"host:{host}"
    if separator_like_title(title):
        return "separator-title"
    return None


def split_noise_entries(
    entries: Sequence[BookmarkEntry],
) -> Tuple[List[BookmarkEntry], List[Tuple[BookmarkEntry, str]]]:
    clean_entries: List[BookmarkEntry] = []
    excluded_entries: List[Tuple[BookmarkEntry, str]] = []
    for entry in entries:
        reason = noise_reason_for_entry(entry)
        if reason:
            excluded_entries.append((entry, reason))
        else:
            clean_entries.append(entry)
    return clean_entries, excluded_entries


def pick_title(titles: Iterable[str], fallback: str) -> str:
    clean_titles = [clean_text(title) for title in titles if clean_text(title)]
    if not clean_titles:
        return fallback
    counts = Counter(clean_titles)
    ranked = sorted(counts.items(), key=lambda item: (-item[1], len(item[0]), item[0].lower()))
    return ranked[0][0]


def dedupe_strings(values: Iterable[str]) -> List[str]:
    seen: Set[str] = set()
    out: List[str] = []
    for value in values:
        if value in seen:
            continue
        seen.add(value)
        out.append(value)
    return out


def dedupe_paths(values: Iterable[Sequence[str]]) -> List[List[str]]:
    seen: Set[Tuple[str, ...]] = set()
    out: List[List[str]] = []
    for parts in values:
        key = tuple(parts)
        if key in seen:
            continue
        seen.add(key)
        out.append(list(parts))
    return sorted(out, key=lambda item: (len(item), [part.lower() for part in item]))


def aggregate_entries(entries: Sequence[BookmarkEntry]) -> Dict[str, Dict[str, object]]:
    grouped: Dict[str, Dict[str, object]] = {}
    for entry in entries:
        normalized = normalize_url(entry.url)
        bucket = grouped.setdefault(
            normalized,
            {
                "url": normalized,
                "raw_urls": [],
                "titles": [],
                "category_paths": [],
                "bookmark_count": 0,
                "bookmark_add_dates": [],
                "bookmark_last_modified_dates": [],
            },
        )
        bucket["raw_urls"].append(entry.url)
        bucket["titles"].append(entry.title)
        bucket["category_paths"].append(entry.category_path or [])
        bucket["bookmark_count"] = int(bucket["bookmark_count"]) + 1
        if entry.add_date:
            bucket["bookmark_add_dates"].append(entry.add_date)
        if entry.last_modified:
            bucket["bookmark_last_modified_dates"].append(entry.last_modified)

    records: Dict[str, Dict[str, object]] = {}
    for url, bucket in grouped.items():
        raw_titles = [str(value) for value in bucket["titles"]]
        title = pick_title(raw_titles, fallback=title_from_url(url))
        category_paths = dedupe_paths(bucket["category_paths"])
        if not category_paths:
            category_paths = [[]]
        bookmark_add_dates = dedupe_strings(sorted(str(value) for value in bucket["bookmark_add_dates"]))
        bookmark_last_modified_dates = dedupe_strings(
            sorted(str(value) for value in bucket["bookmark_last_modified_dates"])
        )
        records[url] = {
            "url": url,
            "host": host_from_url(url),
            "title": title,
            "titles": dedupe_strings(sorted(raw_titles)),
            "category_paths": category_paths,
            "bookmark_count": int(bucket["bookmark_count"]),
            "bookmark_add_dates": bookmark_add_dates,
            "bookmark_last_modified_dates": bookmark_last_modified_dates,
        }
    return records


def aggregate_excluded_entries(
    excluded_entries: Sequence[Tuple[BookmarkEntry, str]],
) -> Dict[str, Dict[str, object]]:
    aggregated = aggregate_entries([entry for entry, _ in excluded_entries])
    reasons: DefaultDict[str, Set[str]] = defaultdict(set)
    for entry, reason in excluded_entries:
        reasons[normalize_url(entry.url)].add(reason)
    for url, record in aggregated.items():
        record["filter_reasons"] = sorted(reasons.get(url, set()))
    return aggregated


def load_state(path: Path) -> Dict[str, object]:
    if not path.exists():
        return {"version": STATE_VERSION, "records": {}}
    payload = load_json(path)
    if not isinstance(payload, dict):
        raise ValueError(f"invalid state file: {path}")
    records = payload.get("records", {})
    if not isinstance(records, dict):
        raise ValueError(f"invalid records block in state file: {path}")
    return {"version": int(payload.get("version", STATE_VERSION)), "records": records}


def load_annotations(path: Optional[Path]) -> Dict[str, Dict[str, object]]:
    if path is None:
        return {}
    payload = load_json(path)
    out: Dict[str, Dict[str, object]] = {}
    if isinstance(payload, dict):
        iterable = [{"url": url, **(data if isinstance(data, dict) else {})} for url, data in payload.items()]
    elif isinstance(payload, list):
        iterable = [item for item in payload if isinstance(item, dict)]
    else:
        raise ValueError("annotations JSON must be an object or a list")
    for item in iterable:
        raw_url = clean_text(str(item.get("url", "")))
        if not raw_url:
            continue
        description = clean_text(str(item.get("description", "")))
        note = clean_text(str(item.get("note", "")))
        raw_tags = item.get("tags", [])
        tags: List[str] = []
        if isinstance(raw_tags, list):
            tags = [clean_text(str(tag)) for tag in raw_tags if clean_text(str(tag))]
        payload = {
            "description": description,
            "note": note,
            "tags": dedupe_strings(tags),
        }
        for url in {raw_url, normalize_url(raw_url)}:
            out[url] = payload
    return out


