def collapse_summary_root(
    folder_children: DefaultDict[Tuple[str, ...], Set[str]]
) -> Tuple[str, ...]:
    current: Tuple[str, ...] = tuple()
    while True:
        children = sorted(folder_children.get(current, set()), key=str.lower)
        if len(children) != 1:
            return current
        current = current + (children[0],)


def principal_categories(
    summary_root: Tuple[str, ...],
    folder_children: DefaultDict[Tuple[str, ...], Set[str]],
    folder_all_urls: DefaultDict[Tuple[str, ...], Set[str]],
    limit: int = 30,
) -> List[Dict[str, object]]:
    items: List[Dict[str, object]] = []
    for child in sorted(folder_children.get(summary_root, set()), key=str.lower):
        child_key = summary_root + (child,)
        items.append(
            {
                "name": child,
                "count": len(folder_all_urls.get(child_key, set())),
                "path": list(child_key),
            }
        )
    items.sort(key=lambda item: (-int(item["count"]), str(item["name"]).lower()))
    return items[:limit]


def render_link_summary_line(
    record: Dict[str, object],
    heading_level: int = 3,
    from_file: Optional[Path] = None,
    note_path: Optional[Path] = None,
    include_domain: bool = True,
    include_categories: bool = True,
) -> str:
    title = str(record.get("title", "Untitled"))
    description = clean_text(str(record.get("description", ""))) or t("pending_description")
    categories = "; ".join(display_category_path(parts) for parts in record.get("category_paths", [[]]))
    heading_prefix = "#" * max(1, min(6, heading_level))
    lines = [
        f"{heading_prefix} {title}",
        "",
        f"- {t('label_url')}: [{record.get('url', '')}]({record.get('url', '')})",
        f"- {t('section_purpose')}: {description}",
    ]
    if include_domain:
        lines.insert(3, f"- {t('label_domain')}: `{record.get('host', '')}`")
    if include_categories:
        insert_at = 4 if include_domain else 3
        lines.insert(insert_at, f"- {t('label_categories_short')}: {categories}")
    if from_file is not None and note_path is not None:
        rel = markdown_rel_path(from_file, note_path)
        lines.append(f"- {t('label_entry_note')}: [{t('label_open_entry')}]({rel})")
    note = clean_text(str(record.get("note", "")))
    if note:
        lines.append(f"- {t('section_note')}: {note}")
    return "\n".join(lines)


def shard_key_for_value(value: str) -> str:
    cleaned = clean_text(value)
    if not cleaned:
        return "other"
    first = cleaned[0].lower()
    if first.isdigit():
        return "0-9"
    if "a" <= first <= "z":
        return first
    return "other"


def shard_key_for_host(value: str) -> str:
    cleaned = clean_text(value).lower()
    if cleaned.startswith("www."):
        cleaned = cleaned[4:]
    token = "".join(ch for ch in cleaned if ch.isalnum())
    if not token:
        return "other"
    if token[0].isdigit():
        return "0-9"
    if len(token) >= 2 and token[0].isalpha() and token[1].isalpha():
        return f"{token[0]}{token[1]}"
    if token[0].isalpha():
        return token[0]
    return "other"


def shard_sort_key(key: str) -> Tuple[int, str]:
    if key == "0-9":
        return (0, key)
    if len(key) == 1 and key.isalpha():
        return (1, key)
    return (2, key)


def shard_display_name(key: str) -> str:
    return key.upper() if len(key) == 1 and key.isalpha() else key


