def render_change_report(
    title: str,
    urls: Sequence[str],
    state: Dict[str, object],
    note_paths: Optional[Dict[str, Path]],
    output_path: Path,
) -> str:
    records_map = state.get("records", {})
    if not isinstance(records_map, dict):
        records_map = {}
    lines = [f"# {title}", ""]
    if not urls:
        lines.append(t("empty_report"))
        return "\n".join(lines) + "\n"
    lines.append(f"- {t('label_counts')}: {len(urls)}")
    lines.append("")
    for url in urls:
        record = records_map.get(url)
        if not isinstance(record, dict):
            continue
        title_text = str(record.get("title", url))
        description = clean_text(str(record.get("description", ""))) or t("pending_description")
        categories = "; ".join(display_category_path(parts) for parts in record.get("category_paths", [[]]))
        lines.append(f"## {title_text}")
        lines.append("")
        lines.append(f"- {t('label_url')}: [{url}]({url})")
        lines.append(f"- {t('label_domain')}: `{record.get('host', '')}`")
        lines.append(f"- {t('label_categories_short')}: {categories}")
        lines.append(f"- {t('section_purpose')}: {description}")
        if record.get("status") == "active" and note_paths:
            rel = markdown_rel_path(output_path, note_paths[url])
            lines.append(f"- {t('links_title')}: [{title_text}]({rel})")
        else:
            lines.append(f"- {t('label_status')}: {t('status_removed' if record.get('status') == 'removed' else 'status_active')}")
        lines.append("")
    return "\n".join(lines).rstrip() + "\n"


