def remove_path(path: Path) -> None:
    if not path.exists():
        return
    if path.is_symlink() or path.is_file():
        path.unlink()
        return
    shutil.rmtree(path)


def sync_path_in_place(source: Path, target: Path) -> None:
    if source.is_dir():
        if target.exists() and not target.is_dir():
            remove_path(target)
        target.mkdir(parents=True, exist_ok=True)
        source_names = {child.name for child in source.iterdir()}
        target_names = {child.name for child in target.iterdir()}
        for stale_name in sorted(target_names - source_names):
            remove_path(target / stale_name)
        for child in source.iterdir():
            sync_path_in_place(child, target / child.name)
        return

    if target.exists() and target.is_dir():
        remove_path(target)
    target.parent.mkdir(parents=True, exist_ok=True)
    temp_target = target.parent / f".{target.name}.codex-tmp"
    if temp_target.exists():
        remove_path(temp_target)
    shutil.copy2(source, temp_target)
    os.replace(temp_target, target)


def replace_target(target_root: Path, stage_root: Path) -> None:
    target_root.mkdir(parents=True, exist_ok=True)
    stage_names = {child.name for child in stage_root.iterdir()}
    for managed_name in sorted(MANAGED_TOP_LEVEL_NAMES):
        source = stage_root / managed_name
        target = target_root / managed_name
        if source.exists():
            sync_path_in_place(source, target)
        elif target.exists():
            remove_path(target)


def is_within(path: Path, parent: Path) -> bool:
    try:
        path.resolve().relative_to(parent.resolve())
        return True
    except ValueError:
        return False


def stash_state_root_extras(state_root: Path, stash_root: Path) -> None:
    core_entries = {
        STATE_FILE,
        PENDING_ANNOTATIONS_FILE,
        EXCLUDED_LINKS_JSON,
        LATEST_SUMMARY_FILE,
        "rendered",
    }
    if not state_root.exists():
        return
    stash_root.mkdir(parents=True, exist_ok=True)
    for child in state_root.iterdir():
        if child.name in core_entries:
            continue
        destination = stash_root / child.name
        if child.is_dir():
            shutil.copytree(child, destination, dirs_exist_ok=True)
        else:
            destination.parent.mkdir(parents=True, exist_ok=True)
            shutil.copy2(child, destination)


