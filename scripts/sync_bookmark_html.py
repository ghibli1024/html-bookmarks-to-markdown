def build_category_tree(
    records: Dict[str, Dict[str, object]]
) -> Tuple[
    DefaultDict[Tuple[str, ...], Set[str]],
    DefaultDict[Tuple[str, ...], List[Dict[str, object]]],
    DefaultDict[Tuple[str, ...], Set[str]],
]:
    folder_children: DefaultDict[Tuple[str, ...], Set[str]] = defaultdict(set)
    folder_direct_links: DefaultDict[Tuple[str, ...], List[Dict[str, object]]] = defaultdict(list)
    folder_all_urls: DefaultDict[Tuple[str, ...], Set[str]] = defaultdict(set)
    folder_all_urls[tuple()] = set()

    for record in sort_records(records.values()):
        url = str(record.get("url", ""))
        category_paths = record.get("category_paths", [[]])
        if not isinstance(category_paths, list) or not category_paths:
            category_paths = [[]]
        for raw_parts in category_paths:
            parts = [clean_text(str(part)) for part in raw_parts if clean_text(str(part))]
            folder_direct_links[tuple(parts)].append(record)
            for depth in range(len(parts)):
                parent = tuple(parts[:depth])
                folder_children[parent].add(parts[depth])
            for depth in range(len(parts) + 1):
                folder_all_urls[tuple(parts[:depth])].add(url)
    return folder_children, folder_direct_links, folder_all_urls


def build_category_fs_map(
    folder_children: DefaultDict[Tuple[str, ...], Set[str]]
) -> Dict[Tuple[str, ...], Path]:
    mapping: Dict[Tuple[str, ...], Path] = {tuple(): ROOT_CATEGORIES}

    def visit(parent: Tuple[str, ...]) -> None:
        used: Dict[str, str] = {}
        for child in sorted(folder_children.get(parent, set()), key=lambda value: value.lower()):
            base = sanitize_filename(child, default="Category")
            candidate = base
            if candidate in used and used[candidate] != child:
                candidate = f"{base}-{short_hash(child, 6)}"
            used[candidate] = child
            child_key = parent + (child,)
            mapping[child_key] = mapping[parent] / candidate
            visit(child_key)

    visit(tuple())
    return mapping


