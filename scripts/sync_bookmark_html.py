class BookmarkHTMLParser(HTMLParser):
    def __init__(self) -> None:
        super().__init__(convert_charrefs=True)
        self.entries: List[BookmarkEntry] = []
        self.folder_stack: List[str] = []
        self.dl_stack: List[bool] = []
        self.pending_folder: Optional[str] = None
        self.capture_tag: Optional[str] = None
        self.capture_attrs: Dict[str, str] = {}
        self.capture_text: List[str] = []

    def handle_starttag(self, tag: str, attrs: List[Tuple[str, Optional[str]]]) -> None:
        lower_tag = tag.lower()
        attr_map = {k.lower(): (v or "") for k, v in attrs}
        if lower_tag in {"a", "h3"}:
            self.capture_tag = lower_tag
            self.capture_attrs = attr_map
            self.capture_text = []
            return
        if lower_tag == "dl":
            opened_for_folder = self.pending_folder is not None
            if opened_for_folder:
                self.folder_stack.append(self.pending_folder or "")
                self.pending_folder = None
            self.dl_stack.append(opened_for_folder)

    def handle_endtag(self, tag: str) -> None:
        lower_tag = tag.lower()
        if lower_tag == "h3" and self.capture_tag == "h3":
            folder_name = clean_text("".join(self.capture_text))
            if folder_name:
                self.pending_folder = folder_name
            self.capture_tag = None
            self.capture_attrs = {}
            self.capture_text = []
            return
        if lower_tag == "a" and self.capture_tag == "a":
            raw_url = self.capture_attrs.get("href", "").strip()
            if raw_url:
                title = clean_text("".join(self.capture_text)) or title_from_url(raw_url)
                self.entries.append(
                    BookmarkEntry(
                        url=raw_url,
                        title=title,
                        category_path=list(self.folder_stack),
                        add_date=self.capture_attrs.get("add_date", "").strip(),
                        last_modified=self.capture_attrs.get("last_modified", "").strip(),
                    )
                )
            self.capture_tag = None
            self.capture_attrs = {}
            self.capture_text = []
            return
        if lower_tag == "dl" and self.dl_stack:
            opened_for_folder = self.dl_stack.pop()
            if opened_for_folder and self.folder_stack:
                self.folder_stack.pop()

    def handle_data(self, data: str) -> None:
        if self.capture_tag:
            self.capture_text.append(data)


