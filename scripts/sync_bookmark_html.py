def collect_compact_category_branches(
    records: Dict[str, Dict[str, object]],
    summary_root: Tuple[str, ...],
) -> Dict[Tuple[str, ...], Set[Tuple[str, ...]]]:
    branches: DefaultDict[Tuple[str, ...], Set[Tuple[str, ...]]] = defaultdict(set)
    for record in records.values():
        if record.get("status") != "active":
            continue
        for parts in record.get("category_paths", [[]]):
            clean_parts = tuple(str(part) for part in parts)
            if len(clean_parts) < len(summary_root) + 1:
                continue
            top_key = clean_parts[: len(summary_root) + 1]
            branch_key = clean_parts[: len(summary_root) + 2] if len(clean_parts) >= len(summary_root) + 2 else top_key
            branches[top_key].add(branch_key)
    return dict(branches)


