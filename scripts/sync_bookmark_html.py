def host_detail_shard_key(record: Dict[str, object]) -> str:
    url = str(record.get("url", ""))
    parsed = urlparse(url)
    parts = [part for part in parsed.path.split("/") if part]
    token_source = parts[0] if parts else str(record.get("title", ""))
    token = "".join(ch for ch in clean_text(token_source).lower() if ch.isalnum())
    if not token:
        return "other"
    if token[0].isdigit():
        return "0-9"
    if len(token) >= 2 and token[0].isalpha() and token[1].isalpha():
        return f"{token[0]}{token[1]}"
    if token[0].isalpha():
        return token[0]
    return "other"


def build_link_shards(
    records: Dict[str, Dict[str, object]]
) -> Tuple[Dict[str, List[Dict[str, object]]], Dict[str, Dict[str, List[Dict[str, object]]]]]:
    host_groups: DefaultDict[str, List[Dict[str, object]]] = defaultdict(list)
    for record in sort_records(records.values()):
        host_groups[str(record.get("host", "unknown-host"))].append(record)

    general_shards: DefaultDict[str, List[Dict[str, object]]] = defaultdict(list)
    heavy_host_shards: Dict[str, Dict[str, List[Dict[str, object]]]] = {}

    for host, host_records in host_groups.items():
        if len(host_records) >= HEAVY_HOST_THRESHOLD:
            subshards: DefaultDict[str, List[Dict[str, object]]] = defaultdict(list)
            for record in host_records:
                subshards[host_detail_shard_key(record)].append(record)
            heavy_host_shards[host] = {
                key: sort_records(items) for key, items in sorted(subshards.items(), key=lambda item: shard_sort_key(item[0]))
            }
        else:
            general_shards[shard_key_for_host(host)].extend(host_records)

    return (
        {key: sort_records(items) for key, items in sorted(general_shards.items(), key=lambda item: shard_sort_key(item[0]))},
        heavy_host_shards,
    )


def build_domain_shards(
    domain_groups: Dict[str, List[Dict[str, object]]]
) -> Dict[str, List[Tuple[str, List[Dict[str, object]]]]]:
    shards: DefaultDict[str, List[Tuple[str, List[Dict[str, object]]]]] = defaultdict(list)
    for host, records in domain_groups.items():
        key = shard_key_for_value(host)
        shards[key].append((host, sort_records(records)))
    for key in shards:
        shards[key].sort(key=lambda item: item[0].lower())
    return dict(shards)


