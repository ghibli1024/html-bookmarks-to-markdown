def merge_records(
    current_records: Dict[str, Dict[str, object]],
    previous_state: Dict[str, object],
    annotations: Dict[str, Dict[str, object]],
    source_html: Path,
    mode: str,
    missing_policy: str,
) -> Tuple[Dict[str, object], Dict[str, List[str]]]:
    previous_records = previous_state.get("records", {}) if mode == "merge" else {}
    if not isinstance(previous_records, dict):
        previous_records = {}

    timestamp = now_iso()
    merged: Dict[str, Dict[str, object]] = {}
    changes: Dict[str, List[str]] = {
        "new": [],
        "changed": [],
        "removed": [],
        "unchanged": [],
        "reactivated": [],
    }

    for url in sorted(current_records):
        current = dict(current_records[url])
        previous = previous_records.get(url) if isinstance(previous_records, dict) else None
        previous = previous if isinstance(previous, dict) else None
        annotation = annotations.get(url, {})

        # In merge mode, keep existing category placement for URLs we already know.
        # This preserves the user's established archive taxonomy instead of
        # reclassifying the whole archive from the latest HTML export.
        if mode == "merge" and previous:
            previous_category_paths = previous.get("category_paths", [])
            if isinstance(previous_category_paths, list) and previous_category_paths:
                current["category_paths"] = previous_category_paths
        previous_source_paths = previous.get("source_category_paths", []) if previous else []
        current_source_paths = current.get("source_category_paths", [])
        if (not isinstance(current_source_paths, list) or not current_source_paths) and isinstance(previous_source_paths, list):
            current["source_category_paths"] = previous_source_paths

        description = clean_text(str(annotation.get("description") or (previous or {}).get("description", "")))
        note = clean_text(str(annotation.get("note") or (previous or {}).get("note", "")))
        existing_tags = previous.get("tags", []) if previous else []
        new_tags = annotation.get("tags", existing_tags)
        tags = dedupe_strings(str(tag) for tag in new_tags if clean_text(str(tag)))

        current["description"] = description
        current["note"] = note
        current["tags"] = tags
        current["status"] = "active"
        current["first_seen_at"] = (previous or {}).get("first_seen_at", timestamp)
        current["last_seen_at"] = timestamp
        current["removed_at"] = ""
        current["source_html"] = str(source_html)
        current["source_signature"] = source_fingerprint(current)

        if previous is None:
            changes["new"].append(url)
        else:
            prev_signature = str(previous.get("source_signature", ""))
            curr_signature = str(current["source_signature"])
            if previous.get("status") == "removed":
                changes["reactivated"].append(url)
                changes["changed"].append(url)
            elif prev_signature != curr_signature:
                changes["changed"].append(url)
            else:
                changes["unchanged"].append(url)
        merged[url] = current

    if mode == "merge":
        for url, previous in previous_records.items():
            if url in merged:
                continue
            if not isinstance(previous, dict):
                continue
            if noise_reason_for_record(str(previous.get("url", url)), str(previous.get("title", ""))):
                continue
            carried = dict(previous)
            if missing_policy == "keep":
                carried["status"] = "active"
                carried["removed_at"] = ""
                merged[url] = carried
                changes["unchanged"].append(url)
            else:
                if carried.get("status") != "removed":
                    carried["status"] = "removed"
                    carried["removed_at"] = timestamp
                    changes["removed"].append(url)
                merged[url] = carried

    state = {
        "version": STATE_VERSION,
        "last_sync_at": timestamp,
        "source_html": str(source_html),
        "records": merged,
    }
    return state, changes


def build_pending_annotations(records: Dict[str, Dict[str, object]]) -> List[Dict[str, object]]:
    pending: List[Dict[str, object]] = []
    for url in sorted(records):
        record = records[url]
        if record.get("status") != "active":
            continue
        if clean_text(str(record.get("description", ""))):
            continue
        pending.append(
            {
                "url": url,
                "title": str(record.get("title", "")),
                "host": str(record.get("host", "")),
                "category_paths": list(record.get("category_paths", [])),
                "description": "",
                "note": str(record.get("note", "")),
                "tags": list(record.get("tags", [])),
            }
        )
    return pending


