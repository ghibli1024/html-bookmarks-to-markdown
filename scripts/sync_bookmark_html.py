def shard_key_for_value(value: str) -> str:
    cleaned = clean_text(value)
    if not cleaned:
        return "other"
    first = cleaned[0].lower()
    if first.isdigit():
        return "0-9"
    if "a" <= first <= "z":
        return first
    return "other"


def shard_key_for_host(value: str) -> str:
    cleaned = clean_text(value).lower()
    if cleaned.startswith("www."):
        cleaned = cleaned[4:]
    token = "".join(ch for ch in cleaned if ch.isalnum())
    if not token:
        return "other"
    if token[0].isdigit():
        return "0-9"
    if len(token) >= 2 and token[0].isalpha() and token[1].isalpha():
        return f"{token[0]}{token[1]}"
    if token[0].isalpha():
        return token[0]
    return "other"


def shard_sort_key(key: str) -> Tuple[int, str]:
    if key == "0-9":
        return (0, key)
    if len(key) == 1 and key.isalpha():
        return (1, key)
    return (2, key)


def shard_display_name(key: str) -> str:
    return key.upper() if len(key) == 1 and key.isalpha() else key


